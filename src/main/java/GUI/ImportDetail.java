    private void Edit_NHEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Edit_NHEditActionPerformed
        // TODO add your handling code here:
        if(txt_vegetableIDEdit.getText().equals("")||txt_VegetableNameEdit.getText().equals("")||
                txt_UnitEdit.getText().equals("")||txt_AmountEdit.getText().equals("")||
                txt_ImageEdit.getText().equals("")||txt_PriceEdit.getText().equals("") ){
            JOptionPane.showMessageDialog(rootPane, "Vui lòng không để trống");
        }
        else{
            int repose=JOptionPane.showConfirmDialog(rootPane, "Đồng ý sửa ?","Confirm", JOptionPane.YES_NO_OPTION);
            if(repose==JOptionPane.YES_OPTION){
            vegetable vg = bLL.getProductByID(Integer.valueOf(txt_vegetableIDEdit.getText()));
            vg.setCatagoryID(traveidsanpham(Box_categoryNameEdit.getSelectedItem().toString()));
           vg.setVegetableName(txt_VegetableNameEdit.getText());
           vg.setUnit(txt_UnitEdit.getText());
           vg.setAmount(Integer.valueOf(txt_AmountEdit.getText()));
           vg.setImage(txt_ImageEdit.getText());
           vg.setPrice(Float.valueOf(txt_PriceEdit.getText()));
           JOptionPane.showMessageDialog(rootPane, "Sửa thành công!");
           bLL.updateProduct(vg);
           Quaylai();
        }
        }
    }//GEN-LAST:event_Edit_NHEditActionPerformed

    private void Delete_NHEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Delete_NHEditActionPerformed
        // TODO add your handling code here:
       int repose=JOptionPane.showConfirmDialog(rootPane, "Đồng ý xóa ?","Confirm", JOptionPane.YES_NO_OPTION);
       if(repose==JOptionPane.YES_OPTION){
           vegetable vg = bLL.getProductByID(Integer.valueOf(txt_vegetableIDEdit.getText()));
            bLL.deleteProduct(vg);
            JOptionPane.showMessageDialog(rootPane, "Đã xóa");
            Quaylai();
        }
    }//GEN-LAST:event_Delete_NHEditActionPerformed
  public category traveidsanpham(String name){
        List<category> catelist= bLL.loadCategory();
        for (int i = 0; i < catelist.size(); i++) {
            if(catelist.get(i).getName().equals(name)){
                return catelist.get(i);
            }
        }
        return null;
    }
    public void Quaylai(){
        ImportReport jf= new ImportReport();
        jf.setVisible(true);
        jf.setLocationRelativeTo(null);
        this.dispose();
    }
