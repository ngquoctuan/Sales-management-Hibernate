    private void showcombobox(){
        Box_categoryNameEdit.removeAllItems();
        List<category> list = bLL.loadCategory();
        for (int i = 0; i < list.size(); i++) {
            Box_categoryNameEdit.addItem(list.get(i).getName());
        }
    }
    private void getCategoryName(String ID){
        String name;
        int Ids= Integer.valueOf(ID);
        vegetable idvg = bLL.getProductByID(Ids);
        category cate= bLL.getDanhMuc(idvg.getCatagoryID().getCatagoryID());
        name=cate.getName();
        Box_categoryNameEdit.setSelectedItem(name);
    }
