    private void btn_addneworderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addneworderActionPerformed
        // TODO add your handling code here:
        if(txt_orderID.getText().equals("")||txt_note.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "You must fill the ID, Note!");
            
        }
        else{
          boolean kt=true;
        int so = Integer.parseInt(txt_orderID.getText());
        List<orderproduct> list = hdBLL.loadOrder();
        for (int i = 0; i < list.size(); i++) {
            if(list.get(i).getOrderID()==so){
                JOptionPane.showMessageDialog(rootPane, "The ID has already existed, please select another ID!");
                kt=false;
                }
            }
        if(kt==true ){
            if(tb_ordr_detail.getRowCount()==0){
            saveOrder();
            }
            else{
                saveOrder();
                for(vegetable vg : vglis){
                    orderproduct odp = hdBLL.getOrder(Integer.valueOf(txt_orderID.getText()));
                    orderdetail ord1 = new orderdetail();
                    ord1.setOrderID(odp);
                    ord1.setVegetableID(vg);
                    ord1.setQuantity(vg.getAmount());
                    ord1.setPrice(vg.getPrice());
                    StatisticsBLL tkBLL = new StatisticsBLL();
                    tkBLL.addOrderDetail(ord1);
                }
            }
        }
        }
    }//GEN-LAST:event_btn_addneworderActionPerformed

