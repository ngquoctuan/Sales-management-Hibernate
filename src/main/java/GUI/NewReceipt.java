    private void btn_new_spActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_new_spActionPerformed
        // TODO add your handling code here:
        int rows=tb_sp.getSelectedRow();
        if(rows<0){
            JOptionPane.showMessageDialog(rootPane, "Select the line to add to the receipt!");
        }
        else{
                vegetable vg = new vegetable();
                vg.setVegetableID(Integer.valueOf(tb_sp.getValueAt(rows, 0).toString()));
                vg.setVegetableName(tb_sp.getValueAt(rows, 1).toString());
                vg.setUnit(tb_sp.getValueAt(rows, 2).toString());
                vg.setAmount(1);
                vg.setImage(tb_sp.getValueAt(rows, 4).toString());
                vg.setPrice(Float.valueOf(tb_sp.getValueAt(rows, 5).toString()));
                
                    int test= 0;
                    
                    for(vegetable vtest :vglis){
                        if(vtest.getVegetableID()==vg.getVegetableID()){
                            JOptionPane.showMessageDialog(rootPane, "This product has already been added!");
                            test++;
                            }
                   }
                    if(test==0){
                   vglis.add(vg);
                   Object[][] modelOBJ;
                   modelOBJ = nhaphangBLL.convertList(vglis);
                   String[] title = {"VegetableID ","VegetableName","Unit","Amount","Image","Price"};
                   DefaultTableModel model = new DefaultTableModel(modelOBJ,title);
                   tb_ordr_detail.setModel(model);
                   tongcong+=vg.getPrice();
                   txt_total.setText(String.valueOf(tongcong));
                   }
        }
    }//GEN-LAST:event_btn_new_spActionPerformed

    private void bt_del_detailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_del_detailActionPerformed
        // TODO add your handling code here:
       int rows=tb_ordr_detail.getSelectedRow();
        if(rows<0){
            JOptionPane.showMessageDialog(rootPane, "Select the line to delete!");
        }
        else{
            DefaultTableModel models = (DefaultTableModel)tb_ordr_detail.getModel(); 
            tongcong-=Float.valueOf(tb_ordr_detail.getValueAt(rows, 5).toString());
            txt_total.setText(String.valueOf(tongcong));
            vglis.remove(rows);
            models.removeRow(rows);
            
            
        }
    }//GEN-LAST:event_bt_del_detailActionPerformed

