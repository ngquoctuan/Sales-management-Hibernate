    public void saveOrder(){
        try {
            orderproduct od = new orderproduct();
            od.setOrderID(Integer.valueOf(txt_orderID.getText()));
            od.setCustomerID(traveidkhachhang(cb_cus.getSelectedItem().toString()));
            od.setDate(new SimpleDateFormat("yyyy-MM-dd").parse(txt_date.getText()));
            od.setTotal(Float.valueOf(txt_total.getText()));
            od.setNote(txt_note.getText());
            hdBLL.addOrder(od);
            JOptionPane.showMessageDialog(rootPane, "Đã thêm mới");
             Quaylai();
              } catch (ParseException ex) {
                  Logger.getLogger(NewReceipt.class.getName()).log(Level.SEVERE, null, ex);
              }
    }
    public customers traveidkhachhang(String name){
    CustomersBLL cusBLL = new CustomersBLL();
        List<customers> cslist= cusBLL.loadCustomers();
        for (int i = 0; i < cslist.size(); i++) {
            if(cslist.get(i).getFullname().equals(name)){
                return cslist.get(i);
            }
        }
        return null;
    }
